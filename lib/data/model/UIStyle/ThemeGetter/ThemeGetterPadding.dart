class ThemeGetterPadding{
  late double paddingSmall;
  late double paddingNormal;
  late double paddingLarge;

  ThemeGetterPadding(double _paddingSmall, double _paddingNormal, double _paddingLarge){
    paddingSmall = _paddingSmall;
    paddingNormal = _paddingNormal;
    paddingLarge = _paddingLarge;
  }
}